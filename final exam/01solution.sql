Q1. The Production.Product table contains a column named Name and a column named Price. You want to write a query that returns the values in these columns. In your resultset, you want the values in the Name column to be returned in a column named ProductName. Which two of the following SELECT statements can you use?

a. SELECT Name AS ProductName, Price FROM Production.Product;
b. SELECT ProductName, Price FROM Production.Product;
c. SELECT Name ProductName, Price FROM Production.Product;
d. SELECT Name, ProductName, Price FROM Production.Product;
e. SELECT Name, Price FROM Production.Product AS ProductName;

# Answer: a,c
